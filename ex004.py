msg = input('Digit something: ')
print('Type', type(msg))
print('Just space?', msg.isspace())
print('Is a number?', msg.isnumeric())
print('Is alphabetic?', msg.isalpha())
print('Is alphanumeric?', msg.isalnum())
print('Is upper?', msg.isupper())
print('Is lower?', msg.islower())
print('Is title?', msg.istitle())


